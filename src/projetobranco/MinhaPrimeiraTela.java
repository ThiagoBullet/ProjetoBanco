    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int numero = Integer.parseInt(jTextFieldNumero.getText());
        double saldo = Double.parseDouble(jTextFieldSaldo.getText());
        double limite = Double.parseDouble(jTextFieldLimite.getText());
        
        String cpf = jTextFieldCPF.getText();
        String nome = jTextFieldNome.getText();
        String end = jTextFieldEndereco.getText();
        String qtdtitular = jTextFieldQtdTitular.getText();
        
        Conta aux = new Conta(numero, saldo, limite, nome, end, cpf);
        contas.add(aux);
        
        jTextFieldNome.setText("");
        jTextFieldNumero.setText("");
        jTextFieldCPF.setText("");
        jTextFieldLimite.setText("");
        jTextFieldEndereco.setText("");
        jTextFieldSaldo.setText("");
        jTextFieldQtdTitular.setText("");
        
        atualizaTela();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed
        // TODO add your handling code here:
        jTextArea.setText("");
        for (Conta c : contas) {
            jTextArea.append(c.toString());
        }
        
        atualizaTela();
    }//GEN-LAST:event_jButtonListarActionPerformed

