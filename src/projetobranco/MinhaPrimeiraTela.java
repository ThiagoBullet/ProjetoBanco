    private void jComboBoxContaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBoxContaItemStateChanged
        // TODO add your handling code here:
        jTextFieldNumero.setText("");
        jTextFieldSaldo.setText("");
        jTextFieldLimite.setText("");

        DefaultListModel<Pessoa> model = new DefaultListModel<>();
        jListTitulares.setModel(model);
        
        Conta auxConta = (Conta)jComboBoxConta.getSelectedItem();
        if(auxConta != null){
            jTextFieldNumero.setText("" + auxConta.getNumero());
            jTextFieldSaldo.setText("" + auxConta.getSaldo());
            jTextFieldLimite.setText("" + auxConta.getLimite());

            model.addElement(null);
            for(Pessoa p : auxConta.getTitular()){
                model.addElement(p);
            }
        
        }
    }//GEN-LAST:event_jComboBoxContaItemStateChanged

